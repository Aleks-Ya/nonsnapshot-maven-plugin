package at.nonblocking.maven.nonsnapshot;

/**
 * An artifact base version to calculate the new version if an update is necessary.
 * <br/><br/>
 * This class should be in the model package, but Maven would not be able to load it during configuration processing.
 * 
 * @author Juergen Kofler
 */
public class BaseVersion {

    private String groupId;
    private String artifactId;
    private String version;

    public BaseVersion() {
        
    }
    
    public BaseVersion(String groupId, String artifactId, String version) {
        this.groupId = groupId;
        this.artifactId = artifactId;
        this.version = version;
    }

    public String getGroupId() {
        return groupId;
    }

    public void setGroupId(String groupId) {
        this.groupId = groupId;
    }

    public String getArtifactId() {
        return artifactId;
    }

    public void setArtifactId(String artifactId) {
        this.artifactId = artifactId;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

}
